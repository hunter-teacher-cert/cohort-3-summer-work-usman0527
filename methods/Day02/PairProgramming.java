import java.io.*;
import java.util.*;

// {4,2,3,1}

public class PairProgramming
{
  public static int barGraphify(int[] x)
{
  int x = 0;
  for (int i = 0; i < x.length; i++)
    {
      System.out.println("=");
    }
    return x;
}  

public static void main (String[] args)
  {
    int[] testArray = {0,1,2,3};
    barGraphify(testArray);
  }
}